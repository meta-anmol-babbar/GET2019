package question1;

public class KeyValuePair {

	public int key;
	public String value;

	/**
	 * This is constructor of keyvaluepair for initialization of data.
	 * 
	 * @param key
	 *            , key of data.
	 * @param value
	 *            , value of data.
	 */
	KeyValuePair(int key, String value) {
		this.key = key;
		this.value = value;

	}

}
